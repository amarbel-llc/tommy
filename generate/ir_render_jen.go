			g.If(jen.Err().Op(":=").Qual(o.ImportPath, decFn).Call(
				jen.Op("&").Add(o.Tgt.Jen().Clone()), ctx.docVar.Clone(), jen.Id("_ch"), ctx.consumed.Clone(), pk.Jen(),
			), jen.Err().Op("!=").Nil()).Block(ctx.retErr(bk+": %w", jen.Err()))
