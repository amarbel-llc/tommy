func jenPosOp(ctx jenCtx, op DecodeOp, pk TOMLKey) []jen.Code {
	switch o := op.(type) {
	case InTable:
		return jenPIT(ctx, o, pk)
	case GetMapStringString:
		return jenPMSS(ctx, o, pk)
	case ForArrayTable:
		return jenPFAT(ctx, o, pk)
	case InPointerTable:
		return jenPIPT(ctx, o, pk)
	case ForMapStringStruct:
		return jenPFMS(ctx, o, pk)
	case DelegateStruct:
		return jenDS(ctx, o)
	case DelegateSlice:
		return jenDSl(ctx, o, "")
	case DelegateMap:
		return jenDM(ctx, o)
	case WithFoundVar:
		return jenPosOp(ctx, o.Inner, pk)
	}
	return nil
}

func posHeader(pk TOMLKey) jen.Code {
	return jen.If(jen.Id("_rc").Dot("Kind").Op("==").Qual(cstPkg, "NodeArrayTable").Op("&&").Qual(cstPkg, "TableHeaderKey").Call(jen.Id("_rc")).Op("==").Add(pk.Jen())).Block(
		jen.If(jen.Id("_pi").Op(">").Id("i")).Block(jen.Break()),
		jen.Id("_pi").Op("++"), jen.Continue(),
	)
}

