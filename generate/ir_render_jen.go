// --- Map key injection ---

func delegateParts(typeName string) (string, string) {
	if i := strings.IndexByte(typeName, '.'); i >= 0 {
		return typeName[:i], typeName[i+1:]
	}
	return "", typeName
}

// jenType returns a jennifer Code for a type name that might be cross-package.
// If importPath is non-empty, uses Qual; otherwise uses Id.
func jenType(typeName, importPath string) *jen.Statement {
	if importPath != "" {
		_, short := delegateParts(typeName)
		return jen.Qual(importPath, short)
	}
	return jen.Id(typeName)
}

func injectMapKey(ops []DecodeOp, mapVar string, parentKey TOMLKey) []DecodeOp {
	// Build the parent's literal suffix to match against inner key literals.
	// parentKey might be dynamic (has KeyPrefix), so we extract the literal tail.
	var parentLitSuffix string
	for _, p := range parentKey.Parts {
		if p.Kind == KeyLit {
			parentLitSuffix = p.Value
		}
	}
	matchSuffix := parentLitSuffix + "."

	mkPre := parentKey.Lit(".").Var(mapVar).Lit(".")
	rk := func(k TOMLKey) TOMLKey {
		// Replace the last literal part's matching prefix with the map key injection.
		// e.g., [KeyPrefix, KeyLit(".targets.host")] → [KeyPrefix, KeyLit(".targets."), KeyVar("_mk"), KeyLit(".host")]
		for i := len(k.Parts) - 1; i >= 0; i-- {
			if k.Parts[i].Kind == KeyLit {
				s := k.Parts[i].Value
				if strings.HasPrefix(s, matchSuffix) {
					rest := s[len(matchSuffix):]
					// Build new key: parts before this literal + mkPre parts + rest
					newParts := make([]KeyPart, i)
					copy(newParts, k.Parts[:i])
					for _, mp := range mkPre.Parts {
						newParts = append(newParts, mp)
					}
					if rest != "" {
						newParts = append(newParts, KeyPart{Kind: KeyLit, Value: rest})
					}
					return TOMLKey{Parts: newParts}
				}
				// For keys with a dot-separated prefix matching, try suffix match
				if idx := strings.Index(s, matchSuffix); idx >= 0 {
					rest := s[idx+len(matchSuffix):]
					pre := s[:idx]
					newParts := make([]KeyPart, i)
					copy(newParts, k.Parts[:i])
					if pre != "" {
						newParts = append(newParts, KeyPart{Kind: KeyLit, Value: pre})
					}
					for _, mp := range mkPre.Parts {
						newParts = append(newParts, mp)
					}
					if rest != "" {
						newParts = append(newParts, KeyPart{Kind: KeyLit, Value: rest})
					}
					return TOMLKey{Parts: newParts}
				}
				break
			}
		}
		return k
	}
	result := make([]DecodeOp, len(ops))
	for i, op := range ops {
		result[i] = injectMapKeyOp(op, rk)
	}
	return result
}

